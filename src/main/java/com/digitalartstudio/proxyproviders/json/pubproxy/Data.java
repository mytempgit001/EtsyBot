package com.digitalartstudio.proxyproviders.json.pubproxy;

public class Data {
	
	private String ipPort;
	private String ip;
	private int port;
	private String country;
	private String last_checked;
	private String proxy_level;
	private String type;
	private String speed;
	private Support support;
	public String getIpPort() {
		return ipPort;
	}
	public void setIpPort(String ipPort) {
		this.ipPort = ipPort;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getLast_checked() {
		return last_checked;
	}
	public void setLast_checked(String last_checked) {
		this.last_checked = last_checked;
	}
	public String getProxy_level() {
		return proxy_level;
	}
	public void setProxy_level(String proxy_level) {
		this.proxy_level = proxy_level;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public Support getSupport() {
		return support;
	}
	public void setSupport(Support support) {
		this.support = support;
	}
}
